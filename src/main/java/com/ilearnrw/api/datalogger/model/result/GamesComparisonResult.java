package com.ilearnrw.api.datalogger.model.result;

public class GamesComparisonResult {
	String school;
	String classroom;
	String username;
	String timeSpentPlaying;
	String daysPlayed;
	String activitiesPlayed;
	String skillsPracticed;
	String wordsSeen;
	String changesToProfile;
	String successRate;

	public String getSchool() {
		return school;
	}

	public void setSchool(String school) {
		this.school = school;
	}

	public String getClassroom() {
		return classroom;
	}

	public void setClassroom(String classroom) {
		this.classroom = classroom;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getTimeSpentPlaying() {
		return timeSpentPlaying;
	}

	public void setTimeSpentPlaying(String timeSpentPlaying) {
		this.timeSpentPlaying = timeSpentPlaying;
	}

	public String getDaysPlayed() {
		return daysPlayed;
	}

	public void setDaysPlayed(String daysPlayed) {
		this.daysPlayed = daysPlayed;
	}

	public String getActivitiesPlayed() {
		return activitiesPlayed;
	}

	public void setActivitiesPlayed(String activitiesPlayed) {
		this.activitiesPlayed = activitiesPlayed;
	}

	public String getSkillsPracticed() {
		return skillsPracticed;
	}

	public void setSkillsPracticed(String skillsPracticed) {
		this.skillsPracticed = skillsPracticed;
	}

	public String getWordsSeen() {
		return wordsSeen;
	}

	public void setWordsSeen(String wordsSeen) {
		this.wordsSeen = wordsSeen;
	}

	public String getChangesToProfile() {
		return changesToProfile;
	}

	public void setChangesToProfile(String changesToProfile) {
		this.changesToProfile = changesToProfile;
	}

	public String getSuccessRate() {
		return successRate;
	}

	public void setSuccessRate(String successRate) {
		this.successRate = successRate;
	}

}
